  SELECT "dog" AS kind, 4 AS legs, 20 AS weight union
  SELECT "cat"        , 4        , 10           union
  SELECT "ferret"     , 4        , 10           union
  SELECT "parrot"     , 2        , 6            union
  SELECT "penguin"    , 2        , 10           union
  SELECT "t-rex"      , 2        , 12000;